// São usados para realizar operações lógicas. Elas podem ser do tipo and (&&), or (||), not (!), e diferente (!= ou !==)  Os operandos devem ser lógicos, (verdadeiro ou falso). Também podem operar sobre expressões lógicas, ou seja, que retornem valores verdadeiros ou falsos. 

/* Para utilizar cada tipo de operador lógico, basta que saibamos as condições de cada um:

AND: retorna true somente se todas as condições forem verdadeiras
OR: retorna true se uma das condições for verdadeira
NOT: inverte o resultado obtido
Diferente: retorna true ou false a partir de uma comparação

E (&&)
OU (||)
Negação (!)
*/

    console.log(2<3 && 3>2);
    console.log(2<3 && 3<2);
    console.log(2<3 || 3<2);
