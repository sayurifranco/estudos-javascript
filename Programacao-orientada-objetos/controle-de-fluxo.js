/** Como qualquer linguagem de programação, o Javascript 
 * possui um fluxo de execução que pega os trechos de código,
 * executa e retorna algum valor em tela ou como resultado 
 * de uma função. Nem sempre esse processo ocorre de maneira
 * satisfatória, pois podem ocorrer exceções e erros. 
 * Alguns motivos para o aparecimento de erros são:
 * 
 * Erro de sintaxe;
 * Erro de referência;
 * Atribuição de tipo errada de uma variável;
 * Utilização de parâmetros errados;
 * Conversão entre variáveis inadequada;
 * Variáveis sem valores recebendo métodos;
 * 
 * 
 * Cada linguagem de programação tem sua própria forma de 
 * lidar com erros. No JavaScript, algumas categorias 
 * disponíveis para os erros são:
 * 
 * RangeError: quando o código recebe um dado do tipo certo,
 * porém fora do formato aceitável. 
 * Isso pode acontecer se usarmos uma variável que 
 * necessite de um valor inteiro positivo, mas em algum 
 * momento passe a receber um valor negativo.
 * 
 * ReferenceError: ocorre quando o código tenta acessar 
 * algo que não existe, como uma variável que não foi 
 * definida.
 * 
 * SyntaxError: às vezes codificamos algo errado no programa
 * e o JavaScript não consegue executá-lo. Os erros podem 
 * ser métodos ou propriedades escritos ou utilizados de 
 * forma incorreta, esquecimento de algum sinal ou item 
 * necessário para condição.
 * 
 * TypeError: indica que o código esperava receber um dado
 * de um determinado tipo, por exemplo uma string, mas 
 * recebeu outro, como um número, booleano ou null. 
 */